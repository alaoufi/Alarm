class HgItem {
  final IconData icon;
  final String title;
  final String desc;
  const HgItem(this.icon, this.title, this.desc);
}

class HgSection {
  final IconData icon;
  final Color color;
  final bool expanded;
  final String title;
  final List<HgItem> items;
  const HgSection(this.icon, this.color, this.expanded, this.title, this.items);
