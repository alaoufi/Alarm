/// يبني أقسام الدليل من قائمة نصوص اللغة (عنوان القسم ثم عنوان/وصف لكل عنصر).
List<HgSection> _build(List<List<String>> langStrings) {
  final out = <HgSection>[];
  for (var s = 0; s < _sectionIcons.length; s++) {
    final strs = langStrings[s];
    final icons = _itemIcons[s];
    final items = <HgItem>[];
    for (var i = 0; i < icons.length; i++) {
      items.add(HgItem(icons[i], strs[1 + i * 2], strs[2 + i * 2]));
    }
    out.add(
        HgSection(_sectionIcons[s], _sectionColors[s], s == 0, strs[0], items));
  }
  return out;
}

